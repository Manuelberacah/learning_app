  // Helper function to shuffle options while keeping track of correct answer
  static List<CourseContent> _shuffleOptions(List<CourseContent> contents) {
    final random = Random();
    
    return contents.map((content) {
      final correctAnswer = content.correctAnswer;
      final shuffledOptions = List<String>.from(content.options)..shuffle(random);
      
      return CourseContent(
        id: content.id,
        type: content.type,
        question: content.question,
        options: shuffledOptions,
        correctAnswer: correctAnswer,
        imageUrl: content.imageUrl,
        audioUrl: content.audioUrl,
      );
    }).toList();
  }

